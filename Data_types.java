public class Data_types {

    public static void main(String args[])
    {
        byte b = 127;
        int i = 18;
        int l = 20300;
        float f = 20.11f;
        double db = 20.20;
        System.out.println(b);
        System.out.println(i);
        System.out.println(l);
        System.out.println(db);
        System.out.println(f);

    }
}
